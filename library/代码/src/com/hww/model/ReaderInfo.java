package com.hww.model;

public class ReaderInfo {
	String readerName;
	String sex;
	String age;
	String zjtype;
	String identityCard;
	String bjDate;
	String maxNumb;
	String keepMoney;
	public String getReaderName() {
		return readerName;
	}
	public void setReaderName(String readerName) {
		this.readerName = readerName;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public String getZjtype() {
		return zjtype;
	}
	public void setZjtype(String zjtype) {
		this.zjtype = zjtype;
	}
	public String getIdentityCard() {
		return identityCard;
	}
	public void setIdentityCard(String identityCard) {
		this.identityCard = identityCard;
	}
	public String getBjDate() {
		return bjDate;
	}
	public void setBjDate(String bjDate) {
		this.bjDate = bjDate;
	}
	public String getMaxNumb() {
		return maxNumb;
	}
	public void setMaxNumb(String maxNumb) {
		this.maxNumb = maxNumb;
	}
	public String getKeepMoney() {
		return keepMoney;
	}
	public void setKeepMoney(String keepMoney) {
		this.keepMoney = keepMoney;
	}

}
